      <h5>So what is Azure?</h5>
<p> Azure is Microsoft’s cloud computing platform. Azure is a collection of services similar to AWS for data, storage, applications, vm’s, general data files, and much more. Azure is a cloud hosting provider that will allow a user to accomplish just about anything from machine learning, to application development. </p>

