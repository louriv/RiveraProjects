      <p> Microsoft is currently offering a free virtual training for Microsoft Azure Fundamentals. This is a 2 day training and there are currently 3 different sets of training dates. Each day consists of ~3 hours of live virtual instruction. While instruction is virtual, microsoft specialists are live to help you with any questions you may have. The link below is the availability in February for those who want to register. </p>


