      <p>Today is a pretty big day for me. Today I decided it would be in my best interest to discontinue running Rivera Projects, LLC (my small business). I can't even describe how much effort and time I have put into making it come this far. It has turned from a college freelancer to a fully functional business on the books. I learned about taxes, profit & loss, Finance, account management, Client Relations, (More) Web Design work, and best of all: <u>What it takes to run a business</u>. This has given me so much more respect for people who choose to do this for full time work. It has always been a dream of mine to run my very own business, and I am lucky enough to say that I accomplished that goal. While I have many reasons for letting this go, the biggest is realizing I would much rather work a 9-5 than a 24/7. I wouldn't by any means look at this as a failure, but more as the most useful class of my life. The biggest takeaway I got from this experience would be the absolute resilience it takes to understand 8 majors and try your best to constantly switch gears to fill whatever role was needed. I will continue to freelance if the opportunity arises because I think this will help get me back to freelancing for my passion, rather than trying to sustain. I can' thank everyone enough who has supported me and been there to offer advice. What I would consider to be my biggest accomplishment would be ending my first year on the books while in the green. Not many people can bill over 10k in their first year, and I am so proud of that fact. Finally, I did want to take this time to give a huge shout out to Nina Ortiz for the constant hard work and effort she put into it with me. She had no prior web design knowledge and is now able to put up an entire site from scratch. That's impressive in my book and i hope that you got just as much takeaway as I did from this experience. Thanks to all who were a part in some way!
